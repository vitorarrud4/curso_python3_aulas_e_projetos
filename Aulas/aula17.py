# if / elif         / else
# se / se nao se    / se nao

condicao = False
condicao2 = False
condicao3 = False
condicao4 = False

if condicao:
    print('Este e o codigo do if')
elif condicao2:
    print('A condicao 2 foi satisfeita')
elif condicao3:
    print('A condicao 3 foi satisfeita')
elif condicao4:
    print('A condicao 4 foi satisfeita')
else:
    print('Nenhuma condicao satisfeita')
