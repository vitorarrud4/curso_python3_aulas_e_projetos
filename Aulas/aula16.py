# introducao aos blocos de codigo + if / elif  / else

# if / elif         / else
# se / se nao se    / se nao
entrada = input("Voce quer 'entrar' ou 'sair'? ")

if entrada == 'entrar':
    print('Voce entrou no sistema!')
elif entrada == 'sair':
    print('Voce saiu do sistema')
else:
    print('Nenhuma das opcoes...')
