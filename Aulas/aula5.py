'''
Tipo de dado bool (boolean)
Ao questionar algo em algum programa, só existem duas
respostas possíveis: sim (True) ou não (false).
Existem vários operadores para 'questionar'.
Dentre eles o ==, que é um operador lógico que
questiona se um valor é igual ao outro
'''

print(10 == 11)
print(type(10 == 12))
print(10 == 10)
